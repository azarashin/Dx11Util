Dx11ObjectGUIMovie::Dx11ObjectGUIMovie(wchar_t* _filename)
{
	is_running = false; 
	filename = _filename; 
}


Dx11ObjectGUIMovie::~Dx11ObjectGUIMovie(void)
{
}



void Dx11ObjectGUIMovie::Setup(Dx11Context* _context)
{
	LONG width, height; 
	ULONG length; 
	HRESULT hr; 


	is_running = false; 
	hr = CoCreateInstance(CLSID_FilterGraph,
		NULL,
		CLSCTX_INPROC,
		IID_IGraphBuilder,
		(LPVOID *)&pGraphBuilder);

	hr = pGraphBuilder->QueryInterface(IID_IMediaControl, (LPVOID *)&pMediaControl);

	hr = CoCreateInstance(CLSID_SampleGrabber,
		NULL,
		CLSCTX_INPROC_SERVER,
		IID_IBaseFilter,
		(LPVOID *)&pSampleGrabberFilter);

	hr = pSampleGrabberFilter->QueryInterface(IID_ISampleGrabber,
		(LPVOID *)&pSampleGrabber);

	ZeroMemory(&am_media_type, sizeof(am_media_type));
	am_media_type.majortype = MEDIATYPE_Video;
	am_media_type.subtype = MEDIASUBTYPE_RGB32;
	am_media_type.formattype = FORMAT_VideoInfo;
	hr = pSampleGrabber->SetMediaType(&am_media_type);

	hr = pGraphBuilder->AddFilter(pSampleGrabberFilter,
		L"Sample Grabber");

	hr = pMediaControl->RenderFile((wchar_t*)filename.c_str()); // not good cast...
	IPin *pGrabOut = GetOutPin(pSampleGrabberFilter);
	IPin *pRenderIn;// = GetInPin(pRenderer);
	hr = pGrabOut->ConnectedTo(&pRenderIn);

	PIN_INFO pi; 
	hr = pRenderIn->QueryPinInfo(&pi); 
	hr = pGraphBuilder->RemoveFilter(pi.pFilter); 
	pGrabOut->Release();


    hr = CoCreateInstance(CLSID_NullRenderer, 
		NULL,
		CLSCTX_INPROC_SERVER,
		IID_IBaseFilter,
		(LPVOID *)&pNull);

	hr = pGraphBuilder->AddFilter(pNull, L"NullRenderer");
	IPin *pGrabOut2 = GetOutPin(pSampleGrabberFilter);
	IPin *pRenderIn2 = GetInPin(pNull);
	hr = pGraphBuilder->Connect(pGrabOut2, pRenderIn2);
	pGrabOut2->Release();
	pRenderIn2->Release();



	pSampleGrabber->GetConnectedMediaType(&am_media_type);
	VIDEOINFOHEADER *pVideoInfoHeader =
		(VIDEOINFOHEADER *)am_media_type.pbFormat;

	width = pVideoInfoHeader->bmiHeader.biWidth;
	height = pVideoInfoHeader->bmiHeader.biHeight;
	length = am_media_type.lSampleSize;

	pSampleGrabber->SetBufferSamples(TRUE);

	pGraphBuilder->QueryInterface(IID_IMediaEvent, (LPVOID*)&media_event); 

	SetupCanvas(_context, width, height); 
}


void Dx11ObjectGUIMovie::GetPicture(char* buf)
{
	long ecode; 
	LONG_PTR p1, p2; 
	while(S_OK == media_event->GetEvent(&ecode, &p1, &p2, 0)) {	
		switch(ecode) {
			case EC_COMPLETE:
			case EC_END_OF_SEGMENT:
			case EC_WINDOW_DESTROYED:
				Stop(); 
				break; 
		}
		media_event->FreeEventParams(ecode, p1, p2); 
	}

   long nBufferSize = am_media_type.lSampleSize;
   pSampleGrabber->GetCurrentBuffer(&nBufferSize, (LONG*)buf);

}


void Dx11ObjectGUIMovie::Term()
{
	TermCanvas(); 

	media_event->Release(); 
	pSampleGrabber->Release();
	pSampleGrabberFilter->Release();
	pMediaControl->Release();
	pGraphBuilder->Release();
}

void Dx11ObjectGUIMovie::Start()
{
	pMediaControl->Run();
	is_running = true; 
}
void Dx11ObjectGUIMovie::Stop()
{
	pMediaControl->Stop();
	is_running = false; 
}


bool Dx11ObjectGUIMovie::IsRunning()
{
	return is_running; 
}

